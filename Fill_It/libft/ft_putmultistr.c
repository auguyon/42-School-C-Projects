#include "libft.h"

void	ft_putmultistr(char **str)
{
	int i;

	i = 0;
	if (!str)
		return ((void)0);
	while (str[i] != 0)
	{
		ft_putstr(str[i++]);
		ft_putchar('\n');
	}
}
