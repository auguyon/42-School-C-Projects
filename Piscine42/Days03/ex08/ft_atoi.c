int		ft_atoi(char *str)
{
	int a;
	int r;
	int n;

	a = 0;
	r = 0;
	n = 0;
	while (str[a] == 32)
		a++;
	if (str[a] == 43 || str[a] == 32)
		a++;
	if (str[a] == 45)
	{
		n = 1;
		a++;
	}
	if (str[a] < 48 || str[a] > 57)
		return (r);
	while (str[a] >= 48 && str[a] <= 57)
	{
		r = r * 10 + (str[a] - '0');
		a++;
	}
	return (n == 1 ? -r : r);
}
