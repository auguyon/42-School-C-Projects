char	*ft_rot42(char *s)
{
	int i;

	i = -1;
	while (s[++i])
	{
		if (s[i] >= 'a' && s[i] <= 'z')
			s[i] = (s[i] - 'a' + 42) % 26 + 'a';
		if (s[i] >= 'A' && s[i] <= 'Z')
			s[i] = (s[i] - 'A' + 42) % 26 + 'A';
	}
	return (s);
}
